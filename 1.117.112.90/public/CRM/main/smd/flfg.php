            <h2>1、总则</h2>
            <p>第一条为了实施可持续发展战略，预防因规划和建设项目实施后对环境造成不良影响，促进经济、社会和环境的协调发展，制定本法。</p>
            <p>第二条本法所称环境影响评价，是指对规划和建设项目实施后可能造成的环境影响进行分析、预测和评估，提出预防或者减轻不良环境影响的对策和措施，进行跟踪监测的方法与制度。</p>
            <p>第三条编制本法第九条所规定的范围内的规划，在中华人民共和国领域和中华人民共和国管辖的其他海域内建设对环境有影响的项目，应当依照本法进行环境影响评价。</p>
            <p>第四条环境影响评价必须客观、公开、公正，综合考虑规划或者建设项目实施后对各种环境因素及其所构成的生态系统可能造成的影响，为决策提供科学依据。</p>
            <p>第五条国家鼓励有关单位、专家和公众以适当方式参与环境影响评价。</p>
            <p>第六条国家加强环境影响评价的基础数据库和评价指标体系建设，鼓励和支持对环境影响评价的方法、技术规范进行科学研究，建立必要的环境影响评价信息共享制度，提高环境影响评价的科学性。</p>
            <p>国务院环境保护行政主管部门应当会同国务院有关部门，组织建立和完善环境影响评价的基础数据库和评价指标体系。</p>
            <h2>第二章规划的环境影响评价</h2>
            <p>第七条国务院有关部门、设区的市级以上地方人民政府及其有关部门，对其组织编制的土地利用的有关规划，区域、流域、海域的建设、开发利用规划，应当在规划编制过程中组织进行环境影响评价，编写该规划有关环境影响的篇章或者说明。</p>
